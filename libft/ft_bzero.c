void	ft_bzero(void *s, size_t n)
{
	size_t	i;

	i = 0;
	while (*(char *)&s[i])
	{
		while (i < n)
		{
			(*(char *)&s[i]) = '\0';
			i++;
		}
		i++;
	}
}
