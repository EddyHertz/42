void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (*(char *)&s[i])
	{
		while (i < n)
		{
			(*(char *)&s[i]) = c;
			i++;
		}
		i++;
	}
}
